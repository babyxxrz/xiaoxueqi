"""
Prepare YOLO dataset for sandbox plate detection.

Supported annotation formats:
1. Pascal VOC XML, commonly generated by LabelImg.
2. LabelMe JSON, rectangle or polygon shapes.
3. Existing YOLO TXT labels.

Example:
    python backend/training/plate/prepare_yolo_dataset.py \
        --images-dir backend/datasets/plate_sandbox/images \
        --annotations-dir backend/datasets/plate_sandbox/annotations \
        --annotation-format xml \
        --output-dir backend/datasets/plate_sandbox_yolo \
        --class-name plate
"""

from __future__ import annotations

import argparse
import json
import random
import shutil
import sys
import xml.etree.ElementTree as ET
from dataclasses import dataclass
from pathlib import Path
from typing import Iterable, List, Optional, Tuple

IMAGE_EXTENSIONS = {".jpg", ".jpeg", ".png", ".bmp", ".webp"}


@dataclass
class BBox:
    class_id: int
    x_center: float
    y_center: float
    width: float
    height: float

    def clamp(self) -> "BBox":
        x_center = min(max(self.x_center, 0.0), 1.0)
        y_center = min(max(self.y_center, 0.0), 1.0)
        width = min(max(self.width, 0.0), 1.0)
        height = min(max(self.height, 0.0), 1.0)
        return BBox(self.class_id, x_center, y_center, width, height)

    def is_valid(self, min_size: float = 1e-6) -> bool:
        return self.width > min_size and self.height > min_size

    def to_yolo_line(self) -> str:
        b = self.clamp()
        return f"{b.class_id} {b.x_center:.6f} {b.y_center:.6f} {b.width:.6f} {b.height:.6f}"


def list_images(images_dir: Path) -> List[Path]:
    if not images_dir.exists():
        raise FileNotFoundError(f"图片目录不存在：{images_dir}")
    return sorted([p for p in images_dir.rglob("*") if p.suffix.lower() in IMAGE_EXTENSIONS])


def get_image_size(image_path: Path) -> Tuple[int, int]:
    try:
        import cv2  # type: ignore

        img = cv2.imread(str(image_path))
        if img is not None:
            h, w = img.shape[:2]
            return w, h
    except Exception:
        pass

    try:
        from PIL import Image  # type: ignore

        with Image.open(image_path) as img:
            return img.size
    except Exception as exc:
        raise RuntimeError(f"无法读取图片尺寸：{image_path}，请确认已安装 opencv-python 或 Pillow。") from exc


def xyxy_to_yolo(
    xmin: float,
    ymin: float,
    xmax: float,
    ymax: float,
    image_width: int,
    image_height: int,
    class_id: int = 0,
) -> Optional[BBox]:
    xmin = max(0.0, min(float(xmin), float(image_width)))
    xmax = max(0.0, min(float(xmax), float(image_width)))
    ymin = max(0.0, min(float(ymin), float(image_height)))
    ymax = max(0.0, min(float(ymax), float(image_height)))

    if xmax <= xmin or ymax <= ymin:
        return None

    x_center = ((xmin + xmax) / 2.0) / image_width
    y_center = ((ymin + ymax) / 2.0) / image_height
    width = (xmax - xmin) / image_width
    height = (ymax - ymin) / image_height

    bbox = BBox(class_id, x_center, y_center, width, height).clamp()
    return bbox if bbox.is_valid() else None


def find_annotation(image_path: Path, annotations_dir: Path, annotation_format: str) -> Optional[Path]:
    stem = image_path.stem
    if annotation_format == "xml":
        candidate = annotations_dir / f"{stem}.xml"
    elif annotation_format == "json":
        candidate = annotations_dir / f"{stem}.json"
    elif annotation_format == "yolo":
        candidate = annotations_dir / f"{stem}.txt"
    else:
        for suffix in [".xml", ".json", ".txt"]:
            candidate = annotations_dir / f"{stem}{suffix}"
            if candidate.exists():
                return candidate
        return None

    return candidate if candidate.exists() else None


def parse_voc_xml(
    annotation_path: Path,
    image_path: Path,
    class_name: str,
    accept_any_class: bool,
) -> List[BBox]:
    tree = ET.parse(annotation_path)
    root = tree.getroot()

    size_node = root.find("size")
    if size_node is not None:
        width_text = size_node.findtext("width")
        height_text = size_node.findtext("height")
        if width_text and height_text:
            image_width = int(float(width_text))
            image_height = int(float(height_text))
        else:
            image_width, image_height = get_image_size(image_path)
    else:
        image_width, image_height = get_image_size(image_path)

    boxes: List[BBox] = []
    for obj in root.findall("object"):
        name = (obj.findtext("name") or "").strip()
        if not accept_any_class and name != class_name:
            continue

        bndbox = obj.find("bndbox")
        if bndbox is None:
            continue

        xmin = float(bndbox.findtext("xmin") or 0)
        ymin = float(bndbox.findtext("ymin") or 0)
        xmax = float(bndbox.findtext("xmax") or 0)
        ymax = float(bndbox.findtext("ymax") or 0)

        bbox = xyxy_to_yolo(xmin, ymin, xmax, ymax, image_width, image_height, class_id=0)
        if bbox:
            boxes.append(bbox)

    return boxes


def parse_labelme_json(
    annotation_path: Path,
    image_path: Path,
    class_name: str,
    accept_any_class: bool,
) -> List[BBox]:
    data = json.loads(annotation_path.read_text(encoding="utf-8"))
    image_width = data.get("imageWidth")
    image_height = data.get("imageHeight")
    if not image_width or not image_height:
        image_width, image_height = get_image_size(image_path)

    boxes: List[BBox] = []
    for shape in data.get("shapes", []):
        label = str(shape.get("label", "")).strip()
        if not accept_any_class and label != class_name:
            continue

        points = shape.get("points", [])
        if not points:
            continue

        xs = [float(p[0]) for p in points]
        ys = [float(p[1]) for p in points]
        bbox = xyxy_to_yolo(min(xs), min(ys), max(xs), max(ys), int(image_width), int(image_height), class_id=0)
        if bbox:
            boxes.append(bbox)

    return boxes


def parse_yolo_txt(annotation_path: Path) -> List[BBox]:
    boxes: List[BBox] = []
    for line in annotation_path.read_text(encoding="utf-8").splitlines():
        line = line.strip()
        if not line:
            continue
        parts = line.split()
        if len(parts) < 5:
            continue
        try:
            class_id = int(float(parts[0]))
            x, y, w, h = map(float, parts[1:5])
        except ValueError:
            continue
        bbox = BBox(class_id, x, y, w, h).clamp()
        if bbox.is_valid():
            boxes.append(bbox)
    return boxes


def parse_annotation(
    image_path: Path,
    annotation_path: Optional[Path],
    annotation_format: str,
    class_name: str,
    accept_any_class: bool,
) -> List[BBox]:
    if annotation_path is None:
        return []

    suffix = annotation_path.suffix.lower()
    if annotation_format == "auto":
        if suffix == ".xml":
            fmt = "xml"
        elif suffix == ".json":
            fmt = "json"
        elif suffix == ".txt":
            fmt = "yolo"
        else:
            return []
    else:
        fmt = annotation_format

    if fmt == "xml":
        return parse_voc_xml(annotation_path, image_path, class_name, accept_any_class)
    if fmt == "json":
        return parse_labelme_json(annotation_path, image_path, class_name, accept_any_class)
    if fmt == "yolo":
        return parse_yolo_txt(annotation_path)

    raise ValueError(f"不支持的标注格式：{fmt}")


def split_images(
    images: List[Path],
    train_ratio: float,
    val_ratio: float,
    test_ratio: float,
    seed: int,
) -> dict[str, List[Path]]:
    total_ratio = train_ratio + val_ratio + test_ratio
    if abs(total_ratio - 1.0) > 1e-6:
        raise ValueError("train-ratio + val-ratio + test-ratio 必须等于 1.0")

    rng = random.Random(seed)
    shuffled = images[:]
    rng.shuffle(shuffled)

    n = len(shuffled)
    n_train = int(n * train_ratio)
    n_val = int(n * val_ratio)

    train = shuffled[:n_train]
    val = shuffled[n_train : n_train + n_val]
    test = shuffled[n_train + n_val :]

    return {"train": train, "val": val, "test": test}


def copy_or_link(src: Path, dst: Path, mode: str) -> None:
    dst.parent.mkdir(parents=True, exist_ok=True)
    if dst.exists():
        dst.unlink()
    if mode == "copy":
        shutil.copy2(src, dst)
    elif mode == "hardlink":
        try:
            dst.hardlink_to(src)
        except OSError:
            shutil.copy2(src, dst)
    else:
        raise ValueError(f"不支持的 copy-mode：{mode}")


def write_data_yaml(output_dir: Path, class_name: str) -> None:
    content = f"""# YOLO dataset config for sandbox plate detection
path: {output_dir.as_posix()}
train: images/train
val: images/val
test: images/test

names:
  0: {class_name}
"""
    (output_dir / "data.yaml").write_text(content, encoding="utf-8")


def prepare_dataset(args: argparse.Namespace) -> None:
    images_dir = Path(args.images_dir)
    annotations_dir = Path(args.annotations_dir)
    output_dir = Path(args.output_dir)

    images = list_images(images_dir)
    if not images:
        raise RuntimeError(f"未找到图片：{images_dir}")

    splits = split_images(images, args.train_ratio, args.val_ratio, args.test_ratio, args.seed)

    if output_dir.exists() and args.clean:
        shutil.rmtree(output_dir)

    for split_name in ["train", "val", "test"]:
        (output_dir / "images" / split_name).mkdir(parents=True, exist_ok=True)
        (output_dir / "labels" / split_name).mkdir(parents=True, exist_ok=True)

    stats = {"images": 0, "annotated_images": 0, "boxes": 0, "empty_images": 0}

    for split_name, split_images_list in splits.items():
        for image_path in split_images_list:
            relative_name = image_path.name
            target_image = output_dir / "images" / split_name / relative_name
            target_label = output_dir / "labels" / split_name / f"{image_path.stem}.txt"

            copy_or_link(image_path, target_image, args.copy_mode)

            ann = find_annotation(image_path, annotations_dir, args.annotation_format)
            boxes = parse_annotation(
                image_path=image_path,
                annotation_path=ann,
                annotation_format=args.annotation_format,
                class_name=args.class_name,
                accept_any_class=args.accept_any_class,
            )

            target_label.write_text("\n".join([b.to_yolo_line() for b in boxes]) + ("\n" if boxes else ""), encoding="utf-8")

            stats["images"] += 1
            stats["boxes"] += len(boxes)
            if boxes:
                stats["annotated_images"] += 1
            else:
                stats["empty_images"] += 1

    write_data_yaml(output_dir, args.class_name)

    print("YOLO 数据集制作完成。")
    print(f"输出目录：{output_dir.resolve()}")
    print(f"图片总数：{stats['images']}")
    print(f"有标注图片：{stats['annotated_images']}")
    print(f"空标注图片：{stats['empty_images']}")
    print(f"车牌框总数：{stats['boxes']}")
    print(f"配置文件：{(output_dir / 'data.yaml').resolve()}")


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description="Convert sandbox plate annotations to YOLO detection dataset.")
    parser.add_argument("--images-dir", default="backend/datasets/plate_sandbox/images", help="原始图片目录。")
    parser.add_argument("--annotations-dir", default="backend/datasets/plate_sandbox/annotations", help="标注目录。")
    parser.add_argument(
        "--annotation-format",
        default="auto",
        choices=["auto", "xml", "json", "yolo"],
        help="标注格式：auto/xml/json/yolo。LabelImg 推荐 xml。LabelMe 推荐 json。",
    )
    parser.add_argument("--output-dir", default="backend/datasets/plate_sandbox_yolo", help="YOLO 数据集输出目录。")
    parser.add_argument("--class-name", default="plate", help="车牌类别名，默认 plate。")
    parser.add_argument("--accept-any-class", action="store_true", help="接受任意类别名并统一映射为 plate。")
    parser.add_argument("--train-ratio", type=float, default=0.8)
    parser.add_argument("--val-ratio", type=float, default=0.2)
    parser.add_argument("--test-ratio", type=float, default=0.0)
    parser.add_argument("--seed", type=int, default=42)
    parser.add_argument("--copy-mode", choices=["copy", "hardlink"], default="copy", help="图片复制方式。")
    parser.add_argument("--clean", action="store_true", help="转换前清空输出目录。")
    return parser


def main() -> int:
    parser = build_parser()
    args = parser.parse_args()
    try:
        prepare_dataset(args)
        return 0
    except Exception as exc:
        print(f"错误：{exc}", file=sys.stderr)
        return 1


if __name__ == "__main__":
    raise SystemExit(main())
